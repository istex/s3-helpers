import {
  S3Client,
  PutObjectCommand,
  GetObjectCommand,
  ListObjectsV2Command,
  HeadObjectCommand,
} from "@aws-sdk/client-s3";
import { Readable } from "stream";

let s3Client: S3Client | undefined;

function initS3Client(config: { endpoint: string, credentials: { accessKeyId: string, secretAccessKey: string } }) {
  return new S3Client({
    endpoint: config.endpoint,
    credentials: config.credentials,
    forcePathStyle: true,
    region: "us-east-1"
  })
}

export function getS3Client(config?: { endpoint: string, credentials: { accessKeyId: string, secretAccessKey: string } }) {
  if (s3Client == null) {
    if (config == null) {
      throw new Error("S3 client cannot be created with empty configuration. Please consider calling the function with a correct config object.");
    }
    s3Client = initS3Client(config);
  }
  return s3Client;
}

export async function putFileToS3(bucket: string, key: string, file: File, s3Client?: S3Client) {
  s3Client ??= getS3Client();
  await s3Client.send(
    new PutObjectCommand({
      Bucket: bucket,
      Key: key,
      Body: file,
    })
  )
}

export async function getFileFromS3(bucket: string, key: string, s3Client?: S3Client) {
  s3Client ??= getS3Client();
  const response = await s3Client.send(
    new GetObjectCommand({
      Bucket: bucket,
      Key: key
    })
  );
  return response.Body;
}

export function getListObjectsFromS3(bucket: string, prefix: string, maxKeys?: number, delimiter?: string, s3Client?: S3Client) {
  s3Client ??= getS3Client();

  let continuationToken: string | undefined;
  let ended = false;
  let fetching = false;

  const stream: Readable = new Readable({
    objectMode: true,
    async read() {
      if (fetching || ended) return;
      fetching = true;

      try {
        const result = await s3Client.send(new ListObjectsV2Command({
          Bucket: bucket,
          Prefix: prefix,
          Delimiter: delimiter,
          MaxKeys: maxKeys ?? 1000,
          ContinuationToken: continuationToken,
        }));
        if (result.Contents != null) {
          for (const obj of result.Contents) {
            stream.push(obj);
          }
        }

        if (result.IsTruncated != null && result.IsTruncated) {
          if (result.NextContinuationToken == null) {
            throw new Error("NextContinuationToken is undefined.");
          }
          continuationToken = result.NextContinuationToken;
          return;
        }
        ended = true;
        stream.push(null);
      }
      catch (err) {
        stream.destroy(new Error(String(err)));
      }
      finally {
        fetching = false;
      }
    }
  });

  return stream;
}

export async function s3FileExists(bucket: string, key: string, s3Client?: S3Client) {
  s3Client ??= getS3Client();
  try {
    await s3Client.send(
      new HeadObjectCommand({ Bucket: bucket, Key: key })
    );
    return true;
  } catch {
    return false;
  }
}

export async function getHeadObjectFromS3(bucket: string, key: string, s3Client?: S3Client) {
  s3Client ??= getS3Client();
  return await s3Client.send(
    new HeadObjectCommand({ Bucket: bucket, Key: key })
  );
}

// For tests
export function resetS3Client() {
  s3Client = undefined;
}
